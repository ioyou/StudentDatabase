   /**
   Class to output characters onto the console
	*/
   package mylibrary.myio.Screen;
   import java.util.*;
    public class Screen
   {
   
       void print(int x)
      {
         System.out.println(x);
      }
   
       void print (String x)
      {
         System.out.println(x);
      }
       void print (double x)
      {
         System.out.println(x);
      }
       void print (long x)
      {
         System.out.println(x);
      }
   }